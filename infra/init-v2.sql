-- =============================================================================
-- §4. Triggers
-- =============================================================================

-- 4.1 pages.tsv 自动维护（全文索引）
-- aliases 与 title 同权重 A——别名（"腾讯"、"700.HK"、"Tencent"）就是替代标题
-- tokens_zh 优先于 content：应用层用 jieba 切完的空格分隔串
CREATE OR REPLACE FUNCTION update_pages_tsv() RETURNS trigger AS $$
BEGIN
  NEW.tsv :=
    setweight(to_tsvector('simple', coalesce(NEW.title, '')), 'A') ||
    setweight(to_tsvector('simple', coalesce(array_to_string(NEW.aliases, ' '), '')), 'A') ||
    setweight(to_tsvector('simple', coalesce(NEW.tokens_zh, NEW.content, '')), 'B') ||
    setweight(to_tsvector('simple', coalesce(NEW.timeline, '')), 'C');
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS trg_pages_tsv ON pages;
CREATE TRIGGER trg_pages_tsv
  BEFORE INSERT OR UPDATE ON pages
  FOR EACH ROW EXECUTE FUNCTION update_pages_tsv();


-- 4.2 update_time 由应用层维护（每次 UPDATE 显式 SET update_time = NOW()），
--     不在 DB 层做 trigger，便于事务可控、批处理友好。


