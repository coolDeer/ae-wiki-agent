-- -----------------------------------------------------------------------------
-- 2.2 pages — 核心表（万物皆 page）
-- -----------------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS pages (
  id           BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  source_id    TEXT        NOT NULL DEFAULT 'default',
  slug         TEXT        NOT NULL,                            -- 'companies/NVIDIA' / 'sources/arete-NOW-260316'
  type         TEXT        NOT NULL,                            -- 'company' | 'person' | 'industry' | 'concept' | 'source' | 'thesis' | 'output'
  title        TEXT        NOT NULL,

  -- 内容
  content      TEXT        NOT NULL DEFAULT '',                 -- 主体 markdown narrative
  tokens_zh    TEXT,                                            -- 应用层 jieba 切完的空格分隔串（喂 tsv）
  timeline     TEXT        NOT NULL DEFAULT '',                 -- markdown 时间线段落（人读版本）
  frontmatter  JSONB       NOT NULL DEFAULT '{}',
  content_hash TEXT,                                            -- SHA-256(content) 变更检测

  -- 投资强查询字段（type='company' 时填）
  ticker       TEXT,                                            -- 'NOW-US' / '600519.SH'
  exchange     TEXT,
  aliases      TEXT[],
  sector       TEXT,
  sub_sector   TEXT,
  country      TEXT,

  -- 检索
  embedding    vector(1536),
  tsv          tsvector,

  -- 组织 / 权限（继承自 raw_files.org_code 或人工创建时填）
  org_code     TEXT,                                            -- 例 'JG1000'，后续作为多租户/权限边界

  -- 状态
  status       TEXT        NOT NULL DEFAULT 'active',           -- 'active' | 'draft' | 'archived'
  confidence   TEXT,                                            -- 'high' | 'medium' | 'low'

  -- 标准审计字段
  extend       JSONB,
  create_by    VARCHAR(64) NOT NULL DEFAULT '',
  update_by    VARCHAR(64) NOT NULL DEFAULT '',
  create_time  TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  update_time  TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  deleted      SMALLINT    NOT NULL DEFAULT 0
);


