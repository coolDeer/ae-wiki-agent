-- =============================================================================
-- §3. 索引
-- =============================================================================
--
-- 设计原则：所有"自然唯一性"约束改为 partial unique index WHERE deleted = 0。
-- 软删除一行后允许重建相同 key 的新行，避免"灵魂占位"问题。
-- 应用层 ON CONFLICT (col) 仍然能命中（PG 支持 partial 索引作为 conflict target，
-- 前提是 INSERT 行满足索引谓词，本仓所有 INSERT 都默认 deleted=0，自然满足）。
-- =============================================================================

