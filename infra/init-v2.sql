-- =============================================================================
-- LLM Wiki v2 Schema — 投资研究专用
-- =============================================================================
-- 设计文档：doc/architecture.md
--
-- 执行方式（首次部署或重建）：
--   psql -h <host> -U ae_root -d ae_wiki -f init-v2.sql
--
-- 幂等性：所有 CREATE 语句均带 IF NOT EXISTS 或等价；可重复执行。
--
-- 设计原则：
--   1. 万物皆 page（gbrain 思想）
--   2. 投资强查询字段直接进 pages 列
--   3. Facts / Theses / Signals 投资专属表
--   4. 不使用外键约束（应用层控制完整性）
--   5. 不支持物理删除（append-only + soft archive via deleted=1 / status=archived）
--   6. Provenance 一等公民
--   7. 所有表统一审计字段（extend / create_by / update_by / create_time / update_time / deleted）
-- =============================================================================


-- =============================================================================
-- §0. 扩展
-- =============================================================================

CREATE EXTENSION IF NOT EXISTS vector;       -- pgvector：向量类型 + HNSW
CREATE EXTENSION IF NOT EXISTS pg_trgm;      -- 三元组索引：模糊搜索
CREATE EXTENSION IF NOT EXISTS pgcrypto;     -- gen_random_uuid() 等


-- =============================================================================
-- §1. 注意事项
-- =============================================================================
-- update_time 字段不在 DB 层自动维护，由应用层在 UPDATE 时显式 SET update_time = NOW()
-- 所有 INSERT/UPDATE 必须由应用层填 create_by / update_by


